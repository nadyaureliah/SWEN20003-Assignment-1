public class Entities {
    // class to store and update entities x and y coordinates
    double x;
    double y;
    public Entities() {}
    public void setX(double x) {
        this.x = x;
    }
    public void setY(double y) {
        this.y = y;
    }
    public double getX() {
        return x;
    }
    public double getY() {
        return y;
    }
}
